let langBtn = document.getElementById("btn-lang");
let otherLang = document.getElementById("other-lang");
let back = document.getElementById("back");



langBtn.addEventListener("click" , () => {
    otherLang.style.display = "block"
    
})
back.addEventListener("click" , () => {
    otherLang.style.display = "none"
    
})


let showmore = document.querySelector(".showmore");
let x = document.querySelector(".back-btn");
let item = document.querySelector(".items");
let itemLabel = document.querySelector(".labels")


showmore.addEventListener( "click" , () => {
    item.style.display = "block";
    itemLabel.style.display = "none";
    showmore.style.display ="none"
    

})

x.addEventListener("click" , () => {
    item.style.display = "none";
    itemLabel.style.display = "inline";
    showmore.style.display ="inline"
})





